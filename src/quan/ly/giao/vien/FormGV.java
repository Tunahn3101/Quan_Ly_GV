    public Employee getModel(){
        Employee epl = new Employee();
        epl.setMaGV(txtma.getText());
        epl.setTenGV(txtTen.getText());
        epl.setDiaChi(txtdiaChi.getText());
        epl.setNgaySinh(txtngaySinh.getText());
        boolean gt = false;
        if(rdNam.isSelected()){
            gt = true;
        }
        epl.setGioiTinh(gt);
        epl.setPhongBan(txtboMon.getSelectedItem().toString());
        return epl;

    }
       public boolean checkValidateForm(){
           if(txtma.getText().isEmpty()||
                txtngaySinh.getText().isEmpty()||
                   txtTen.getText().isEmpty()||
                   txtdiaChi.getText().isEmpty()){
               return false;
           }
              return true;          
       }
            
    private void txtLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLuuActionPerformed
        // TODO add your handling code here:
        if (checkValidateForm()) {

            Employee e = getModel();
            if (teacher.add(e) > 0) {
                txtthongTin.setText("Cập nhật thành công");
                txtthongTin.setForeground(Color.BLUE);
                txtCount.setText("COUNT: " + teacher.count());
                // Cap nhat du lieu len bang
                
                fillTable();
                
            } else {
                txtthongTin.setText("Cập nhật không thành công");
                txtthongTin.setForeground(Color.RED);
            }
        }else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin");
        }


    }//GEN-LAST:event_txtLuuActionPerformed

    
    public void setModel(Employee lss){
        txtma.setText(lss.getMaGV());
        txtTen.setText(lss.getTenGV());
        txtdiaChi.setText(lss.getDiaChi());
        txtngaySinh.setText(lss.getNgaySinh());
        rdNam.setSelected(lss.isGioiTinh());
        rdNu.setSelected(!lss.isGioiTinh());
        txtboMon.setSelectedItem(lss.getPhongBan());
    }
    
    private void txttimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txttimKiemActionPerformed
        // TODO add your handling code here:
        if(txtma.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã giáo viên");
       
        }
        else {
            Employee lss = teacher.findBymaGV(txtma.getText());
            if(lss !=null){
                setModel(lss);
            } else {
                txtthongTin.setText("Không tìm thấy  tên giáo viên"+ txtma.getText());
                txtthongTin.setForeground(Color.RED);
            }
        }
        
        
    }//GEN-LAST:event_txttimKiemActionPerformed

    private void txtSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSuaActionPerformed
        // TODO add your handling code here:
        
        
        if(txtma.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Chưa nhập mã giáo viên");
        }
        else{
            Employee lss = getModel();
            if(teacher.edit(lss)>0){
             txtthongTin.setText("Sửa thông tin giáo viên thành công");
              txtthongTin.setForeground(Color.BLUE);
              fillTable();
                
            } else {
                txtthongTin.setText("Sửa thông tin không thành công");
                txtthongTin.setForeground(Color.RED);
            }
                
           
            }
                
            
        

        
    }//GEN-LAST:event_txtSuaActionPerformed

