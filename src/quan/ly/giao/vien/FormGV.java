    public void fillTable(){
        DefaultTableModel tbModel = 
                (DefaultTableModel) txtbang.getModel();
        tbModel.setRowCount(0);
        for (Employee listt : teacher.getALLGV()) {
            
            Object [] rowData = new Object[6];
            rowData[0] = listt.getMaGV();
            rowData[1] = listt.getTenGV();
            rowData[2] = listt.getNgaySinh();
            String gt = "Nữ";
            if(listt.isGioiTinh()){
                gt ="Nam";
            }
            
            rowData[3] = gt;
            rowData[4] = listt.getDiaChi();
            rowData[5] = listt.getPhongBan();
             tbModel.addRow(rowData);
            
        }
        
    }

